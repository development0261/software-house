<script src='https://cdnjs.cloudflare.com/ajax/libs/fullcalendar/3.1.0/fullcalendar.min.js'></script>
<script>
    $(document).ready(function() {
        // page is now ready, initialize the calendar...
        $('#calendar').fullCalendar({
            // put your options and callbacks here
            events : [
                @foreach($meetings as $meeting)
                {
                    title : '{{ $meeting->time }}',
                    start : '{{ $meeting->date }}',
                    description : '{{ $meeting->name }}<br>{{ $meeting->email }}<br>{{ $meeting->cellnumber }}'
                },
                @endforeach
            ],
            eventMouseover: function(calEvent, jsEvent) {
                var tooltip = '<div class="tooltipevent" style="padding:10px 20px;border-radius:10px;width:300px;height:auto;background:#fcf8e3;position:absolute;z-index:10001;">' + calEvent.description + '</div>';
                var $tooltip = $(tooltip).appendTo('body');

                $(this).mouseover(function(e) {
                    $(this).css('z-index', 10000);
                    $tooltip.fadeIn('500');
                    $tooltip.fadeTo('10', 1.9);
                }).mousemove(function(e) {
                    $tooltip.css('top', e.pageY + 10);
                    $tooltip.css('left', e.pageX + 20);
                });
            },

            eventMouseout: function(calEvent, jsEvent) {
                $(this).css('z-index', 8);
                $('.tooltipevent').remove();
            },
        })
    });
</script>
